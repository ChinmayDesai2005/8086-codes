DATA SEGMENT
STR1 DB "CHINMAY DESAI$"
STRR DB 20 DUP("$")
DATA ENDS

CODE SEGMENT
ASSUME CS:CODE, DS:DATA
START:
MOV AX, DATA
MOV DS, AX
LEA SI, STR1
LEA DI, STRR
MOV CL, 00H
LENGTH:
MOV AL, [SI]
CMP AL, "$"
JZ NEXT
INC CL
INC SI
JMP LENGTH
NEXT:
DEC SI
REV:
CMP CL, 00H
JZ FINAL
MOV AL, [SI]
MOV [DI], AL
INC DI
DEC SI
DEC CL
JMP REV
FINAL:
MOV AH, 4CH
INT 21H
CODE ENDS
END START




