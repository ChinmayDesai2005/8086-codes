DATA SEGMENT
STR1 DB "THIS IS A STRING$"
VOW DB "AEIOU"
COUNT DB ?
DATA ENDS

CODE SEGMENT
ASSUME CS:CODE, DS:DATA
START:
MOV AX, DATA
MOV DS, AX
LEA SI, STR1
LEA DI, VOW
MOV BX, 0000H
OLOOP:
MOV AL, [SI]
CMP AL, "$"
JZ FINAL
MOV CL, 00H
ILOOP:
CMP CL, 05H
JZ NFND
CMP [DI], AL
JZ FND
INC DI
INC CL
JMP ILOOP
FND:
INC BX
NFND:
INC SI
LEA DI, VOW
JMP OLOOP
FINAL:
MOV COUNT, BL
MOV AH, 4CH
INT 21H
CODE ENDS
END START


