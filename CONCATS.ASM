DATA SEGMENT
STR1 DB "CHINMAY$"
STR2 DB " DESAI$"
STRF DB 20 DUP("$")
DATA ENDS

CODE SEGMENT
ASSUME CS:CODE, DS:DATA
START:
MOV AX, DATA
MOV DS, AX
LEA SI, STR1
LEA BX, STRF
MOV CL, 00H
ADD1:
MOV AL, [SI]
CMP AL, "$"
JZ NEXT1
MOV AL, [SI]
MOV [BX], AL 
INC SI
INC BX
JMP ADD1
NEXT1:
INC CL
CMP CL, 02H
JZ FINAL
LEA SI, STR2
JMP ADD1
FINAL:
MOV AH, 4CH
INT 21H
CODE ENDS
END START


