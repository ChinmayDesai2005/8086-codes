MOV DS, AX
LEA SI, NUM
LEA DI, ODD
LEA BX, EVEN
MOV CL, [SI]
INC SI
MOV AX, 0000H
BACK:
MOV AL, [SI]
MOV DL, AL
ROR DL, 1
JC EVE
MOV [DI], AL
INC DI
JMP NEXT
EVE:
MOV [BX], AL
INC BX
NEXT:
INC SI
LOOP BACK
MOV AH, 4CH
INT 21H
CODE ENDS
END START


